// --- tiny utils ---
static inline uint64_t rol64(uint64_t x, unsigned k){ return (x<<k) | (x>>(64-k)); }
static inline uint32_t rol32(uint32_t x, unsigned k){ return (x<<k) | (x>>(32-k)); }

// --- anti-debug (simple but annoying) ---
static int anti_debug(void){
    if (ptrace(PTRACE_TRACEME, 0, 0, 0) == -1) return -1;
    // Also check TracerPid in /proc/self/status
    FILE *f = fopen("/proc/self/status","r");
    if (!f) return 0;
    char line[256];
    int traced = 0;
    while (fgets(line,sizeof(line),f)){
        if (!strncmp(line,"TracerPid:",10)){
            int v=0; sscanf(line+10,"%d",&v); if (v>0){ traced=1; break; }
        }
    }
    fclose(f);
    return traced?-1:0;
}

