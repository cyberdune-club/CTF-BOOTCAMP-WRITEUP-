// expected hash (obfuscated): we compare (h0 ^ h1) == EXPECTED
// Store EXPECTED ^ 0xA5A5A5A5A5A5A5A5
static const uint64_t obf_expected = 0xD6F61B5B6B6D07B6ULL ^ 0xA5A5A5A5A5A5A5A5ULL;
// key schedule derived from hidden seed (also obfuscated)
static const uint64_t k0 = 0xBADC0FFEE0DDF00DULL ^ 0xA5A5A5A5A5A5A5A5ULL;
static const uint64_t k1 = 0x1337C0DE42424242ULL ^ 0xA5A5A5A5A5A5A5A5ULL;

static void vm_mix(const unsigned char* in, size_t n, uint64_t *out0, uint64_t *out1){
    uint64_t h0 = 0x9e3779b97f4a7c15ULL ^ (k0 ^ 0xA5A5A5A5A5A5A5A5ULL);
    uint64_t h1 = 0xc2b2ae3d27d4eb4fULL ^ (k1 ^ 0xA5A5A5A5A5A5A5A5ULL);
    uint32_t r0 = 0, r1 = 0, r2 = 0;

    for (size_t i=0;i<n;i++){
        uint8_t b = in[i];
        uint8_t t = sbox[(uint8_t)(b ^ (i*29 + 7))];
        r0 = rol32(r0 + t + (uint32_t)i*131, (i%7)+1);
        r1 ^= (uint32_t)(t*0x9E + 0x5F);
        r2 = (r2 + (r1 ^ r0) + (uint32_t)(h0>>32)) * 2654435761u;

        h0 = rol64(h0 + t + r0, 5) ^ (h1 + 0x27d4eb2f165667b1ULL);
        h1 = rol64(h1 ^ (t + r1), 7) + (h0 ^ 0x85ebca6b27d4eb2fULL);
    }

    // final avalanche
    h0 ^= (uint64_t)r0 << 32 | r1;
    h1 ^= (uint64_t)r2 << 17 | (r0 ^ r1);
    h0 += rol64(h1, 13);
    h1 += rol64(h0, 17);
    // murmur-ish fmix
    h0 ^= h0 >> 33; h0 *= 0xff51afd7ed558ccdULL;
    h0 ^= h0 >> 33; h0 *= 0xc4ceb9fe1a85ec53ULL;
    h0 ^= h0 >> 33;
    h1 ^= h1 >> 33; h1 *= 0xff51afd7ed558ccdULL;
    h1 ^= h1 >> 33; h1 *= 0xc4ceb9fe1a85ec53ULL;
    h1 ^= h1 >> 33;

    *out0 = h0; *out1 = h1;
}

static int basic_format_ok(const char *s, size_t n){
    if (n < 18 || n > 80) return 0;
    if (strncmp(s, "CYBERDUNE{", 10) != 0) return 0;
    if (s[n-1] != '}') return 0;
    return 1;
}

int main(void){
    if (anti_debug() != 0){
        puts("nope.");
        return 0;
    }

    char buf[256]={0};
    printf("Enter flag: ");
    fflush(stdout);
    if (!fgets(buf, sizeof(buf), stdin)) return 0;
    size_t n = strlen(buf);
    while (n>0 && (buf[n-1]=='\n' || buf[n-1]=='\r')) buf[--n]=0;

    if (!basic_format_ok(buf, n)){
        puts("format?");
        return 0;
    }

    uint64_t h0,h1;
    vm_mix((const unsigned char*)buf, n, &h0, &h1);
    uint64_t expected = obf_expected ^ 0xA5A5A5A5A5A5A5A5ULL;
    if ( (h0 ^ h1) == expected ){
        puts("OK!");
        // Print back the flag (only if correct)
        puts(buf);
    } else {
        puts("nope.");
    }
    return 0;
}
